const controller = require("@controllers/reports/Tab_24_rpt_PA_Gantt.js");
const { getReport } = require("@validators/report/index.js");
const what = "report";

const routes = [
  {
    method: "GET",
    url: `/${what}/Tab_24_rpt_PA_Gantt`,
    schema: getReport,
    preHandler: controller.Tab_24_rpt_PA_Gantt,
    handler: controller.getReport,
    config: {
      role: "PMO-Reports-Capability",
    },
  },
];
const registerRoutes = (fastify, options, done) => {
  // Ensure all of the routes above get registered.
  routes.forEach((route) => fastify.route(route));
  done();
};

module.exports = {
  registerRoutes,
};
